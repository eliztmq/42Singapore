#include "libft.h"

void	*memset(void *s, int c, size_t n)
{
	size_t			cnt;
	unsigned char	*str;

	cnt = 0;
	str = s;
	while (cnt < n)
	{
		str[cnt] = c;
		cnt++;
	}
	return (s);
}
